package week1.day1;

public class LearnDataTypes {
	
	public static void main(String[] args) {
		
		//DataTypes - primitive/Non-Primitive
		//Primitive DataTypes - Integer, Float, Character, Boolean
		//Non_primitive DataTypes - String, Array
		
		//DataType VariableName = value;
		String userName = "Divya";
		char lastName = 'V';
		int age = 20;
		boolean major = true;
		float height = 175.8f;
		long contactNumber = 98765432112352525l;
		
		System.out.println("userName "+userName);
		System.out.println("lastName "+lastName);
		System.out.println("age "+age);
		System.out.println("major "+major);
		System.out.println("height "+height);
		System.out.println("contactNumber "+contactNumber);
		
			
		
		
	}

}
